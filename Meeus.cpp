#include "Meeus.h"

//******************************************************************************
// Meeus()
//******************************************************************************
Meeus::Meeus() {
  this->dt = QDateTime::currentDateTime();
}

//******************************************************************************
// Meeus()
//******************************************************************************
Meeus::Meeus(QDateTime dt) {
  this->dt = dt;
}

//******************************************************************************
// Meeus()
//******************************************************************************
Meeus::Meeus(Location location, QDateTime dt) {
  this->location = location;
  this->dt = dt;
}

//******************************************************************************
// Meeus()
//******************************************************************************
Meeus::Meeus(Location location) {
  this->location = location;
  this->dt = QDateTime::currentDateTime();
}

//******************************************************************************
// Meeus()
//******************************************************************************
Meeus::Meeus(Location location, QDateTime dt, QTimeZone tz) {
  this->location = location;
  this->dt = dt;
  this->tz = tz;
}

